a=int(input("Enter the first number"))
b=int(input("Enter the second number"))
c=input("Enter your choice :- 1. + 2. - 3. - 4.* 5. % ")
if(c=='1' or c== '+' ):
    print(a+b)
elif(c=='2' or c== '-' ):
    print(a-b)
elif(c=='3' or c== '*' ):
    print(a*b)
elif(c=='4' or c== '/' ):
    print(a/b)
if(c=='5' or c== '%' ):
    print(a%b)



